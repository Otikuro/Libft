#include "libft.h"

char	*ft_strdup(char *s)
{
	int		i;
	char	*copy;

	i = 0;
	if (!s)
		return (NULL);
	copy = (char *)malloc(ft_strlen(s) + 1);
	if (!copy)
		return (NULL);
	while (s[i])
	{
		copy[i] = s[i];
		i++;
	}
	copy[i] = '\0';
	return (copy);
}
